--TAO BANG NHAN VIEN
create table NHANVIEN(
    MANV VARCHAR(10) PRIMARY KEY,
    TENNV NVARCHAR2(50),
    PHAI VARCHAR(10),
    NGAYSINH DATE,
    DIACHI NVARCHAR2(255),
    SODT VARCHAR(14),
    LUONG VARCHAR(100),
    PHUCAP VARCHAR(100),
    VAITRO NVARCHAR2(30),
    MANQL VARCHAR2(10),
    PHG VARCHAR(10)
);


--TAO BANG PHONG BAN
CREATE TABLE PHONGBAN(
    MAPB VARCHAR(10) PRIMARY KEY,
    TENPB NVARCHAR2(30),
    TRPHG VARCHAR(10)
);


--TAO BANG DE AN
CREATE TABLE DEAN(
    MADA VARCHAR(10) PRIMARY KEY,
    TENDA NVARCHAR2(250),
    NGAYBD DATE,
    PHONG VARCHAR(10)
);

--TAO BANG PHAN CONG
CREATE TABLE PHANCONG(
    MANV VARCHAR(10),
    MADA VARCHAR(10),
    THOIGIAN DATE,
    PRIMARY KEY(MANV, MADA)
);

ALTER TABLE NHANVIEN ADD CONSTRAINT NHANVIEN_NHANVIEN
FOREIGN KEY (MANQL) REFERENCES NHANVIEN(MANV);
ALTER TABLE NHANVIEN ADD CONSTRAINT NHANVIEN_PHONGBAN
FOREIGN KEY (PHG) REFERENCES PHONGBAN(MAPB);


ALTER TABLE DEAN ADD CONSTRAINT DEAN_PHONGBAN
FOREIGN KEY (PHONG) REFERENCES PHONGBAN(MAPB);

ALTER TABLE PHANCONG ADD CONSTRAINT PHANCONG_NHANVIEN
FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV);
ALTER TABLE PHANCONG ADD CONSTRAINT PHANCONG_DEAN
FOREIGN KEY (MADA) REFERENCES DEAN(MADA);

